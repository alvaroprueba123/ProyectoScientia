                                </p>
                                    La innovación es un tema bastante subjetivo, hace unos días veía la publicación 
                                    de una de las personas que fue un mentor mío que mencionaba sobre una especie de 
                                    “innovación” que se estaba haciendo en una empresa de gas, y ellos decían que 
                                    lo que estaban haciendo en estos momentos era ofrecerte el servicio de gas, 
                                    pero a través de WhatsApp, o sea estaban tomando estrategias de atención al 
                                    cliente personalizadas y él mencionaba: “esto es innovación”. Yo me puse a refutar, 
                                    ya que para que algo sea innovador tiene que tener dos parámetros, debe tener novedad 
                                    y segundo debe tener utilidad, algo que se está presentando ya sea una actividad, 
                                    un proceso o un producto para considerarlo como innovación debes determinar si tiene 
                                    NOVEDAD (que los usuarios lo consideren novedoso, que difiera significativamente de 
                                    un producto o proceso anterior) y UTILIDAD (que los usuarios lo consideren valioso 
                                    como para adquirirlo, consumirlo y recomendarlo). La delimitación de lo que es novedoso 
                                    y útil depende del usuario, lo que para alguien en Perú puede ser muy novedoso para 
                                    alguien en China tal vez no lo sea, de repente ya lo conocen hace mucho tiempo. 
                                <br>
                                    La definición oficial de innovación según la 4ta edición del Manual de Oslo (2018), 
                                    publicado por la OECD (Organización para la Cooperación y el Desarrollo Económico) 
                                    los componentes que incluyen el papel de la innovación son el conocimiento, la novedad 
                                    y la utilidad, y la creación o preservación de valor. El término hace referencia a una 
                                    actividad (proceso), como también al resultado de dicha actividad (producto).
                                <br>
                                    Diferencia con inventos: Si tienes algo NOVEDOSO a tal punto de que pueda llegar a ser 
                                    PATENTABLE, pero los usuarios no lo encuentran ÚTIL, entonces tienes un INVENTO, 
                                    por lo tanto, podría llegar a decir que muchas investigaciones científicas se quedarían 
                                    en invento si no consiguen entrar al mercado. A nivel de gobernanza el Sistema Nacional 
                                    de Innovación (Sinacyt) está compuesto por el PCM, CONCYTEC, Academia, CITES, Empresas 
                                    y al final los ciudadanos. Entonces desde esta definición puedes ver que realmente la 
                                    innovación, si bien se genera y hay fondos para que las universidades puedan investigar 
                                    o las instituciones hagan productos nuevos, al final todo esto debe llegar a la comunidad.
                                    El gobierno no te da dinero para que hagas una investigación y termines publicando artículos, 
                                    ese no es el fin, lo que se espera es que está investigación termine en un beneficio, en un 
                                    desarrollo para la misma comunidad. Si por ahí puede terminar en una empresa que termine 
                                    generando un producto nuevo o empleo para la sociedad, entonces maravilloso, pero eso depende 
                                    mucho de las políticas del gobierno.
                                    También tenemos la innovación como parte de un modelo empresarial, toma en cuenta dos focos.
