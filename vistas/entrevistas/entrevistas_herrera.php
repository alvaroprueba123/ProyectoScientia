                                    DoKtus, etc.
                                    Ahora, hablando de divulgación científica, podemos decir que es una forma de gestión del conocimiento. 
                                    Consiste en una actividad de relaciones públicas de la comunidad científica 
                                    que se interesa por mostrar a la sociedad los hechos de carácter científico, pero, de una manera más efectiva.
                                    Gestionar el conocimiento es muy importante ya que de esa forma podemos reducir trabajos, si alguna persona esta realizando algunos trabajos y tiene algunos problemas que 
                                    <!------------->
                                    información científica es valiosa, no le va a dar interés. Existe ese paradigma en algunas personas, piensan que la ciencia es difícil, estudiar es aburrido. Nosotros estamos muy acostumbrados a hablar con palabras técnicas y nos gusta decir hablar mucho sobre todo lo que sabemos. Si consideramos una buena gestión de la producción científica encontramos que tanto la innovación como la divulgación son productos de la investigación, mientras que una se encarga de enfocar los esfuerzos de las investigaciones hacia los usuarios, la otra se encarga de popularizar los conocimientos científicos para un público objetivo, el cual también podría llegar ser el usuario final del producto de la investigación.
                                    Actualmente podemos poner como ejemplo las mascarillas. Ahora veo que hay muchas personas que están vendiendo mascarillas de confección peruana siguiendo estándares que ha dado el ministerio. La divulgación trata de resolver las preguntas: ¿Qué tipos de mascarillas hay? ¿Cuál es la más adecuada? ¿Qué mascarillas debo comprar si trabajo en el mercado o en una posta? ¿Cómo usar las mascarillas? ¿Cuántas veces puedo reutilizarlas? Son preguntas que la población quiere saber y debemos centrarnos en responder de la manera más adecuada y rápida, mientras que al hablar de innovación tendríamos que preguntarnos si la población no puede comprarse mascarillas N95 en estos momentos, entonces ¿cómo podemos crear mascarillas más adecuadas para ellos? ¿Como podríamos mejorar las mascarillas para que sean más adaptables a nivel de reducción de costos, materiales?
                                    Ambos términos están enfocados en un bien a la comunidad y enfocados en poder crear algo nuevo.
