                                    Schistocera cancellata. Fuente: Flickr
                                </p>
                                <p>
                                    Los factores climáticos están directamente relacionados con la densidad poblacional de esta especie ya que ponen sus huevos en lugares húmedos [3], en este lugar estos mismos se desarrollan y eclosionan aproximadamente, Según Bar [4] de 50 a 120 huevos y pueden poner huevos de 2-3 veces por año. 
                                </p>
                                <p>
                                    Estas mangas de langostas vienen causando daños desde hace bastante tiempo en varios países, un caso en 2017 fue alarmante debido a que pasó de ser una plaga rural a una urbana, estableciéndose en Agosto en la provincia de Córdoba, Argentina. El 11 de mayo generó gran alarma en Paraguay y unas semanas después ya se encontraba en la provincia de Formosa, en Argentina. Hace unos días fue vista en Corrientes, y fue vista cerca de la frontera con Brasil. Por lo que el ministerio de Agricultura de Brasil declaró en emergencia sanitaria a los estados de Río Grande do Sul y Santa Catarina.[5]
                                </p>
