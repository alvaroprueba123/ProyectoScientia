                                <p>
                                    Actualmente en medio de esta pandemia mundial, una de las plagas más destructivas del mundo está dañando a una gran parte de Sudamérica. Se trata de la especie Schistocera cancellata la cual ha estado provocando daños a nivel de agricultura y economía en los países de: Argentina, Brasil y Paraguay. 
                                </p>
                                <p>
                                    La Schistocera cancellata.,también conocida como ‘’Langosta voladora’’ es un insecto plaga, puede recorrer hasta 150 km/día, dependiendo de la temperatura ya que si esta es baja su movimiento tiende a reducirse, lo que favorece a su migración y pueden comer casi cualquier material vegetal aproximadamente la FAO estima que una manga de un kilómetro cuadrado puede comer lo mismo que 35.000 personas, en Argentina el Senasa estima un consumo similar al de 2.000 vacas por día [1], afectando a los cultivos directamente, la ganadería indirectamente, ya que se comen los alimentos de estos, a plantas nativas causando su extinción  y en consecuencia a la economía. El área de distribución de la especie abarca el centro y norte de Argentina, Uruguay, Paraguay, sudeste de Bolivia, Sur de Brasil, centro y norte de Chile y en Perú también fueron identificadas. Además, tienen una particularidad propia de su especie ya que estas cambian su comportamiento y fisiología dependiendo de la densidad poblacional considerando la ‘’Teoría de Fases’’ de Boris Petrovich Uvarov, esto dificulta poder tener un registro taxonómico de las mismas [2] sobre todo en América donde hay varias especies del género Schistocera.
                                </p>
                                <img src="../imagenes/actualidad/mundo/campuzano/grillo.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
