                                <h5>
                                    ¿Cómo funcionan?
                                </h5>
                                <p>
                                    La termometría infrarroja se basa en que todos los objetos tienen una capacidad de emitir energía infrarroja de sí mismos, la cual indica la temperatura de estos1. Es decir, todo objeto con una temperatura superior al punto cero irradian energía infrarroja (IR), la cual se puede medir con un equipo como estos termómetros. La cantidad de energía que emitan los cuerpos por unidad de superficie se le conoce como emisividad. Este factor es importante, ya que, es una variable para una correcta medición con un termómetro infrarrojo2. 
                                </p>
