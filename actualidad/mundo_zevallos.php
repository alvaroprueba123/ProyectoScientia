                                <h5>30 de Julio, 2020</h5>
                                <h2>¿Héroes silenciosos o silenciados? Realidad de los trabajadores sanitarios en el Perú y el mundo</h2>
                                <h5>Martha Zevallos Alvites</h5>
                                <img src="../imagenes/actualidad/mundo/zevallos/cover.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <br>
                                <p>
                                    Fuente: Cadena política, 2020.
                                </p>
                                <p>
                                    En estos meses de pandemia el mundo ha reconocido la loable labor del personal sanitario, héroes anónimos que han trabajado en silencio para garantizarnos vías públicas seguras y libres de contagio. Hasta hace unas semanas, los limeños saludábamos su valor cada noche con aplausos desde nuestras ventanas y balcones. Sin embargo, la situación de los trabajadores de limpieza no es la ideal.
                                </p>
                                <h5>
                                    Coyuntura internacional
                                </h5>
                                <p>
                                    Según la Oficina de Estadísticas Laborales del Departamento de Trabajo de los Estados Unidos el trabajo sanitario representa el quinto empleo más riesgoso del país, desarrollado por casi 4.4 millones de personas, la mayoría inmigrantes. Por lo tanto, el acceso a subsidios por desempleo o seguros de salud es limitado o en muchos casos, inexistente.
                                </p>
                                <p>
                                    A pesar de la disposición de algunas grandes compañías de mantener la cadena de pagos, se han visto también grandes abusos. En Seattle, los empleados de limpieza denunciaron no haber recibido capacitaciones ni implementos de seguridad adecuados, además de verse forzados a utilizar sus vacaciones o permisos de enfermedad si es que se infectaban con el coronavirus.
                                </p>
                                <p>
                                    Al otro lado del país, en la ciudad de Filadelfia, más de 100 trabajadores sanitarios se reunieron a protestar por mejores condiciones laborales. Esto se dio en respuesta a la propuesta del alcalde por un recorte de presupuesto al Departamento de calles de la ciudad, lo que provocó amenazas de penalidades y posibles pérdidas de sus empleos.
                                </p>
                                <p>
                                    Un poco más al sur, en el vecino país de México, se vive una situación parecida. Los representantes del gremio sanitario de la Ciudad de México indican que, aunque les prometieron insumos y una posible prestación económica por el riesgo a la infección, no han recibido nada.
                                </p>
                                <p>
                                    El asunto se agudiza hacia el oriente, puesto que en la India la situación es incluso más precaria. Cinco millones de personas tienen empleos relacionados a la limpieza pública, incluyendo el recojo manual de desechos humanos de letrinas. Si las condiciones de trabajo desde antes ya eran prácticamente inhumanas, al no contar con elementos de protección ni seguridad social, ahora es mucho peor. 
                                </p>
                                <p>
                                    Adicional a la falta de equipamiento adecuado, se ha sumado a su carga habitual de trabajo la desinfección de los exteriores de viviendas de pacientes infectados con COVID-19. Mientras tanto, las autoridades solo han optado por dictar algunos manuales para el uso seguro de los servicios sanitarios, aunque sin mecanismos fiscalizadores que garanticen su cumplimiento.
                                </p>
                                <h5>
                                    Ámbito local
                                </h5>
                                <p>
                                    Lima no es la excepción a esta problemática. Desde 1996, la responsabilidad de la labor sanitaria recae en la empresa Innova Ambiental. En 2015, durante el periodo de Luis Castañeda, se anunció una nueva convocatoria, la cual fue rechazada en el 2017 por la Corte Suprema al ordenarse el pase a planilla de 709 trabajadores afiliados al Sindicato de Obreros de la Empresa Innova Ambiental de Lima (SITOBUR).
                                </p>
                                <p>
                                    Al no cumplir la comuna metropolitana, se forzó la intervención del Poder Judicial, que ratificó la decisión de la sala superior. Allí se inició una agotadora batalla para las obreras de limpieza pública por los derechos merecidos tras años de servicio, batalla que continua hasta el día de hoy.
                                </p>
                                <p>
                                    Con la nueva gestión, un nuevo drama daba inicio. A solo 9 días de la toma de mando, Jorge Muñoz hacía un pedido a la Corte Suprema para desestimar la resolución previa, solicitud que fue declarada improcedente por la Primera Sala Constitucional. 
                                </p>
                                <p>
                                    Tres años después del primer fallo este sigue sin cumplirse. El burgomaestre, siguiendo los pasos de su predecesor, ha anunciado una nueva licitación de limpieza pública, en la que no se garantiza la continuidad, piso salarial ni derechos ganados tras 23 años de lucha.
                                </p>
                                <p>
                                    En consecuencia, un grupo de trabajadoras alzó su voz de protesta por el respeto de las facultades obtenidas vía judicial y el regreso a sus labores, a pesar de la violenta represión policial a la que se han visto sujetas. La respuesta del alcalde de Lima ha sido hasta ahora bastante insulsa, al indicar que sería la siguiente empresa ganadora quien se encargaría, excusándose de toda responsabilidad y demostrando su intención de continuar con la licitación. 
                                </p>
                                <img src="../imagenes/actualidad/mundo/zevallos/huelga.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                    Fuente: Isabel Cortéz, 2020.
                                </p>
                                <p>
                                    En conclusión, el carácter esencial del personal sanitario alrededor del mundo se ha vuelto innegable en el contexto actual, al igual que las condiciones de trabajo a las que están expuestos. Su rol como primera línea de defensa contra el virus deberá ser valorado por la ciudadanía mediante la resistencia y al apoyo a su lucha, pues si ellos caen, caemos todos.
                                </p>
