                                    Claramunt Vallespí & Claramunt Vallespí en su libro Mujeres en ciencia y tecnología describen historias de diversas mujeres, entre ellas a Jane Goodall, quien en su momento fue la primera persona en introducirse en el mundo de los chimpáces en los años sesenta. Jane Goodall contó sus experiencias a lo largo de su vida y lo plasmó en su libro Gracias a la vida. En este expresó la falta de credibilidad y burlas que recibió por parte del gremio de científicos, dado que no cumplía con el modelo aliñado estandar de una mujer en aquella época generando cierta desconfianza en sí misma. Por otro lado, Mary Anning, famosa recolectora de fósiles y coleccionista del siglo XIX, desde pequeña fue autodidacta y al no ir a la escuela opto por aprender ella misma a escribir, luego tuvo interés de aprender geología, anatomía y la tierra, y la llevaron a estudiar estos temas profundamente. Sin embargo, debido a que Mary era una mujer y tenía antecedentes de clase trabajadora desafortunadamente no pudo unirse a la Sociedad Geológica y, mientras estaba viva, nunca recibió el reconocimiento completo que merecía. Asimismo, Ada Lovelace, quien se refugió en las matemáticas al padecer problemas de salud, creó el primer algoritmo para máquinas; en su momento fue tomada a la ligera hasta llegar al punto de discriminación y no reconocimiento de su gran descubrimiento. 
                                </p>
