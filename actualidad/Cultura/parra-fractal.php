                                <h2>La parra fractal de la Amazonía peruana: la Ayahuasca.</h2>
                                <h5>Por Raihil Rabindranath Rengifo Sánchez</h5>
                                <img src="../../imagenes/actualidad/cultura/img3_rengifo.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
