                                <h5>03 de Agosto, 2020</h5>
                                <h2>La gran fortaleza de los Chachapoyas.</h2>
                                <h5>Por Diana Alexandra Ishara Shimbo</h5>
                                <img src="../../imagenes/actualidad/cultura/img2_ishara.jpg" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <br>
                                <p>
                                Kuélap es una ciudadela fortificada ubicada en la parte alta del valle del río Utcubamba, próximo al caserío de Kuélap en el distrito de Tingo, en el departamento de Amazonas. Con una vista inigualable basada en la concurrencia de la selva amazónica y las alturas de los Andes, este monumento fue construido por la cultura preinca Chachapoyas y, según los historiadores, por el siglo XI, a sus inicios; y posteriormente descubierta en 1843 por Juan Crisóstomo Nieto. 
                                <br>
                                <h5>Materiales de la ciudadela </h5>
                                El principal material para la construcción de esta “fortaleza” fueron los bloques de piedra caliza canteada sin pulimento, los cuales presentaron distintas calidades de acabados. Los más elaborados fueron destinados a la construcción de estructuras ceremoniales.
                                <br>
                                <h5>Arquitectura</h5>
                                Esta ciudadela, con una extensión aproximadamente de 450 hectáreas, fue construida sobre dos inmensas plataformas artificiales superpuestas cuyas murallas logran alcanzar los 20 metros de altura (aproximadamente) en algunos trechos, y presentan diversos centros de vigilancia, un torreón y 3 ingresos, estando uno de ellos orientado hacia el este y los otros dos, hacia el oeste. Las entradas hacia la fortaleza poseen una peculiar forma de embudo, iniciando con un ancho de 3 metros y tornándose cada vez más angosto hasta los 70 centímetros. Esto ha sido supuesto como una medida de control y defensa ya que esta estructura permitiría el paso de una sola persona a la vez.
                                <br>
                                Kuélap está conformada por más de 450 estructuras distribuidas en diversos niveles, de los cuales 4 son de planta rectangular, 1 de planta cuadrangular y las demás de planta circular. Estas construcciones eran de diversas índoles, como administrativa, religiosa, ceremonial y de residencia permanente. 
                                Dentro de este complejo arqueológico se puede observar que está constituido por terrazas de cultivo, asentamientos, accesos de control, almacenes y plazas públicas.
                                <br>
                                <h5>La estrategia de la ubicación</h5>
                                Construido en la cima de un promontorio rocoso de origen calcáreo, Kuélap se encuentra en un punto estratégico para la observación de su periferia; además de la impenetrabilidad por parte de los pueblos próximos debido a las grandes murallas y abismos a sus alrededores
                                
                                </p>
                                <img src="../../imagenes/actualidad/cultura/img3_ishara.jpg" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                Fortaleza de Kuélap. Fuente: Wikipedia
                                </p>
                                <p>
                                <h5>Importantes estructuras</h5>
                                Esta ciudadela presenta, especialmente, 3 estructuras arquitectónicas muy llamativas y sobresalientes, denominadas hoy en día como el "Tintero", el "Torreón Norte" y el "Castillo".
                                </p>
                                <p>
                                <h5>El “Tintero”</h5>
                                "El Tintero" es considerado como una estructura que cumplió un rol religioso (escenario de complejos rituales y ceremonias) por su peculiar forma de cono invertido, lo cual se ha comprendido como observatorio astronómico; además de que en su interior se puede observar una cámara en forma de botella de más de 5 metros de profundidad, lo cual ha llevado a algunos investigadores a pensar que su verdadera función sería la de "chullpa" (mausoleo). “El Tintero” alcanza los 5.5 metros de alto con un diámetro de base de 13.7 metros. 
                                </p>
                                <img src="../../imagenes/actualidad/cultura/img4_ishara.jpg" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                El “Tintero”. Fuente: Peru Top Tours
                                </p>
                                <p>
                                <h5>El “Torreón Norte”</h5>
                                El "Torreón Norte" está construido sobre la segunda plataforma y es considerado como la construcción más alta de Kuélap. Esta estructura, la cual logra alcanzar los 7 metros de alto, se le atribuye una función defensiva. Desde ahí se obtiene una inmejorable vista panorámica de los alrededores. 
                                </p>
                                <p>
                                <h5>El “Castillo”</h5>
                                El “Castillo” es una construcción ceremonial el cual es utilizado como un mausoleo.  
                                </p>
                                <img src="../../imagenes/actualidad/cultura/img5_ishara.jpg" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                El “Castillo”. Fuente: Peru Top Tours
                                </p>
                                <p>
                                <h5>Las Viviendas</h5>
                                Las viviendas de planta circular tienen en promedio un diámetro de 7 metros, y presentan en la cara exterior (fachada) frisos con figuras de rombos hechos con piedras o formando figuras geométricas que se asemejan a aves o personas. Se han reportado evidencias de que los interiores de estas viviendas estuvieron enlucidos con barro y pintados.
                                </p>
                                <p>
                                <img src="../../imagenes/actualidad/cultura/img6_ishara.jpg" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                Las viviendas. Fuente: Arqueología del Perú
                                </p>
                                <p>
                                <h5>La ciudadela en la actualidad</h5>
                                El Complejo Arqueológico “Kuélap”, el cual es el más importante del nororiente del país, fue declarado Patrimonio Cultural de Perú en 1998, favoreciendo su protección y el gran aumento del turismo nacional e internacional.
                                </p>
