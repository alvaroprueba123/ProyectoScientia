                                <h5>03 de Agosto, 2020</h5>
                                <h2>Matemáticas ancestrales: Más que un sincretismo cultural.</h2>
                                <h5>Por Flor Marylin Fernandez Bautista</h5>
                                <img src="../../imagenes/actualidad/cultura/img3_Fernandez.jpg" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
