                                <h5>03 de Agosto, 2020</h5>
                                <h2>Hay Victoria en el negro. </h2>
                                <h5>Por Dayna Milene Ojeda Izquierdo</h5>
                                <img src="../../imagenes/actualidad/cultura/img3_ojeda.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <br>
                                <p>
                                <h5>“… ¿Soy acaso negra?” – me dije ¡SÍ! “¿Qué cosa es ser negra?” ¡Negra!
                                Y yo no sabía la triste verdad que aquello escondía.¡ Negra!
                                Y me sentí negra, ¡Negra!...” (1)
                                </h5>
                                <br>
                                Si reconocieron este fragmento de un poema que tiene un sonoro ritmo, mensaje y poder, pues que bueno que sepan de antemano de qué personaje les expondré a continuación. ¡Y qué personaje! Déjenme decirles.
                                <br>
                                Victoria Eugenia Santa Cruz Gamarra es hasta la actualidad un gran hito y paradigma de la cultura afroperuana, siempre presta a compartir su cosmovisión y filosofía de vida a través del ritmo interior. Nacida el 27 de octubre de 1922 en el barrio de La Victoria, Sebastián Barranca 435 para ser precisos, fue la octava de diez hijos de Nicomedes Santa Cruz y Victoria Gamarra. Creció en un seno familiar muy apegado a las artes. Con un padre dramaturgo, una madre bailarina de zamacueca y marinera, hermanos que compartían la misma pasión por las artes, no era de extrañarse que Victoria lograse ser un ícono en el ámbito artístico. 
                                <br>
                                Tuvo una niñez marcada por la discriminación, uno de los muchos obstáculos que se le presentarían. A pesar del impacto negativo que generó en ella en un comienzo, lo revirtió de la manera más sutil en arte, música y potente mensaje. En el programa “Retratos” producido por TV Perú, Victoria nos cuenta una amarga experiencia sobre la discriminación que vivió en su barrio cuando tuvo entre 5 a 7 años. “En una oportunidad se muda, viene a vivir aquí al barrio, una familia muy blanca. La chiquita era gringuita y cuando salgo yo a jugar la gringuita me mira y dice: Si esa negrita juega yo me voy…” Este hecho generaría en ella una aversión hacia la gente blanca y un dilema consigo misma que la llevaría a buscar en su interior quién realmente era. Pasaría tiempo para que ella resolviera muchos de esos prejuicios; no solo por ser afrodescendiente, sino que también por ser mujer
                                <br>
                                Lejos de esas terribles vivencias, Victoria nunca dejó de lado la música, los cantos y el ritmo que tanto la apasionaba. En 1958, invitada por su hermano, el conocido decimista Nicomedes Santa Cruz, montó y dio vida a una pequeña, pero muy exitosa compañía teatral, Cumanana. En ella presentaron estampas del folclore afrodescendiente, que estaban al borde del olvido. Su hermano Nicomedes por un lado era el orador de las décimas, mientras que Victoria se dedicó asiduamente a la confección y modelaje del vestuario. Tiempo después varios agregados como coreografías, canciones, pregones se iban presentando en diversas funciones y obras de la compañía. Responsabilidades asumidas todas por Victoria, convirtiéndola también en compositora y coreógrafa.
                                </p>
                                <img src="../../imagenes/actualidad/cultura/img4_ojeda.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                Ilustración 1. Victoria y su hermano Nicomedes ensayando en la Compañía Cumanana. Fuente: archivo de Octavio Santa Cruz
                                </p>
                                <p>
                                Con la experiencia que hubo adquiriendo en la compañía de Cumanana, creó y dirigió el drama negro Malató, obra de éxito arrasador que le dio la oportunidad de ser becaria en la Universidad del Teatro de las Naciones (1961) y en la Escuela Superior de Estudios Coreográficos en Francia. Debutó en la obra “Retablillo de don Cristóbal” de Federico García Lorca por su creación y diseño de vestuario.
                                </p>

                                <img src="../../imagenes/actualidad/cultura/img5_ojeda.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                Ilustración 2. Victoria Santa Cruz bailando junto a su compañía Teatro y Danzas Negras del Perú. Fuente: Winston Orrillo, "Magia y realismo en el Teatro y Danzas Negras del Perú", revista Oiga, N° 242, 6 de octubre de 1967, pp. 28-29.
                                </p>
                                <p>
                                Con nuevos conocimientos, experiencia y gran reconocimiento, decide regresar al Perú en 1966 para hacer patria. Por lo que hizo un llamado nacional a todos los negros, y mestizos de negros que quisieran aprender teatro y danzas formando posteriormente su propia compañía, Teatro y Danzas Negras del Perú en 1968. Llegó a reclutar personajes criollos muy conocidos como Lucila Campo, Lalo Izquierdo, Ronaldo Campos, entre otros. Tal fue el éxito de la compañía que representaron al Perú en los Juegos Olímpicos de México (1968), otorgándole una medalla de oro y un diploma por la excelente presentación. 
                                </p>
                                <p>
                                Pasado el tiempo se la nombró directora del recién creado Conjunto Nacional del Folclore (1973-1982) del entonces Instituto Nacional de Cultura (Actual Ministerio de Cultura). El Conjunto Nacional del Folclore contaba con danzas provenientes de las entonces mencionadas tres regiones, Costa, Sierra y ceja de Selva. Se podría pensar que Victoria, al ser representante del folclore costeño no podría lidiar con la difusión de las otras regiones; sin embargo, expuso y dio a conocer de gran manera el folclore andino. Una vez más, producto de su perseverancia, disciplina y total entrega, el Conjunto Nacional del Folclore realizó una gira mundial totalmente subvencionada por capital extranjero.
                                </p>
                                <img src="../../imagenes/actualidad/cultura/img6_ojeda.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                Ilustración 3. Conjunto Nacional del Folclore, Victoria junto a los miembros de la costa. Fuente: Revista COSAS Perú.
                                </p>
                                <p>
                                Sus enseñanzas y compartir del ritmo no se limitaron a Perú, sino que fue invitada a diferentes talleres en países como Italia, Canadá, Rusia, Dinamarca. Además, fue docente en la Universidad Carnegie Mellon en Estados Unidos por 17 años en el Departamento de Drama, enseñando teatro sin ninguna técnica tradicional más que el ritmo interior.
                                <p>
                                <img src="../../imagenes/actualidad/cultura/img7_ojeda.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                Ilustración 4. Victoria Santa Cruz junto a colegas y amigos de la Universidad Carnegie Mellon. Fuente: Revista COSAS Perú.
                                </p>
                                <p>
                                Victoria Santa Cruz nos ha dejado un legado maravilloso a través del ritmo interior, el cual abarca toda su cosmovisión y filosofía de vida como dio a conocer en todas y cada una de las entrevistas que se le han realizado. Su libro “Ritmo, el eterno organizador” nos explica no solo el cómo lidió los obstáculos en su vida, sino también el rol y compromiso que tenemos para buscar nuestra tarea en este mundo sin perder la conexión y el equilibrio en nosotros mismos. Todo a través del ritmo interior. Recordemos que, y cito a esta extraordinaria mujer, “Los obstáculos son las increíbles ventajas de las aparentes desventajas” y que además “las victorias reales son internas…, no se adquieren extirpando el obstáculo sino…, transformándolo” (5) .  Su amor por compartir el ritmo y el de ayudar trascendió llevándola a fundar una organización sin fines de lucro, llamada Salud-Equilibrio-Ritmo. 
                                </p>
                                <p>
                                Canciones, décimas, obras teatrales, exponentes folclóricos maravillosos y nuestro folclore en el ojo del mundo, son algunos de los incontables aportes que esta imponente mujer nos dejó con el propósito inicial de luchar por el despertar del orgullo negro que cambiaría, con el paso de los años, a luchar por la familia humana.
                                </p>
                                <p>
                                Cumpliéndose este 30 de agosto 6 años desde su defunción, continuemos compartiendo y difundiendo nuestro ritmo y folclore. ¿Y cómo hacerlo? No oprimiendo ni minimizando a nuestros compatriotas porque “si la discriminación del blanco al negro es triste, del blanco al indio es triste…Es más triste del negro al negro y del indio al indio”. Reconozcamos el arduo trabajo de personas como Victoria para hacer del Perú un país mejor.
                                </p>
                                <p style="text-align: center;">
                                “Al fin comprendí AL FIN. Ya no retrocedo AL FIN. Y avanzo seguro AL FIN…Y bendigo al cielo porque quiso Dios que negro azabache fuese mi color. Y ya comprendí AL FIN. Ya tengo la llave… ¡Negra soy!”
                                </p>

