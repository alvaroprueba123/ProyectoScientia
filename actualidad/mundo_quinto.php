                                <h5>30 de Julio, 2020</h5>
                                <h2>El 50% de los tiburones del Mediterráneo están heridos</h2>
                                <h5>Katherine Quinto Granda</h5>
                                <img src="../imagenes/actualidad/mundo/quinto/full.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                    Figura 1. Cámaras de video de la ONG Shark Med, muestran a una tintorera arrastrando un anzuelo. (1)
                                </p>
                                <h5>
                                    La mitad de los tiburones del Mar Mediterráneo son víctimas de los anzuelos incrustados en sus cuerpos y desgraciadamente viven con ellos.
                                </h5>
                                <br>
                                <p>
                                    La ONG Shark Med, lleva años estudiando la situación que atraviesan los tiburones en Baleares y en todo el Mediterráneo Occidental. Ellos han ofrecido unos datos que ilustran el declive de estas poblaciones. Según la información proporcionada, la mitad de los tiburones observados (todos ellos de la especie tintorera) presentan anzuelos clavados en sus cuerpos que arrastran largos sedales, algunos con restos de redes y vegetales. En la figura 1 y 2, se observa a 2 individuos con un anzuelo incrustado en su mandíbula. Y en la figura 3, se ve como un tiburón arrastra una red de pescar por el océano. En la mayoría de las cámaras sale la especie de tiburón tintorera, cosa que es preocupante, ya que, en la lista Roja de la Unión Internacional para la Conservación de la Naturaleza (UICN) agrego el año pasado a 17 especies de tiburones por riesgo de extinción. Una de ellos son las tintoreras, o tiburón azul (el más abundante en el Mediterráneo). Además, la lista roja, estima que el 53 % de los tiburones, rayas y quimeras del Mediterráneo están en riesgo de extinción.
                                </p>
                                <img src="../imagenes/actualidad/mundo/quinto/tiburon.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                    Figura 2. Fotografía de ONG Shark Med, donde se ve a una tintorera con una anzuelo incrustado en la mandíbula. (2)
                                </p>
                                <p>
                                    En esta línea, SharkMed y la Fundación Marilles han aunado fuerzas en un ambicioso proyecto en el Mediterráneo occidental para continuar la  labor de recogida de datos sobre tintoreras y otras especies de tiburones en el mar Balear.
                                </p>
                                <p>
                                    El objetivo principal del proyecto es proporcionar, las herramientas necesarias para concienciar sobre la importancia de la recuperación y conservación de los escualos, así como para promover el desarrollo sostenible de las actividades que más les afectan, exigiendo cambios reales y concretos en la pesca, por ejemplo», afirma Agustí Torres, presidente de SharkMed, en el comunicado.
                                </p>
                                <img src="../imagenes/actualidad/mundo/quinto/full2.png" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p style="text-align: center;">
                                    Figura 3. Tiburón del mediterráneo arrastrando una red de pescar. (3)
                                </p>
                                <p>
                                    Para Aniol Esteban, director de la Fundación Marilles, se dedican a financiar proyectos para mejorar el mar balear, "un mar con tiburones es un mar en buen estado de conservación". Asegura que "el único miedo que hay que tener a los tiburones es a su extinción", ya que al encontrarse en la cima de la cadena trófica, son imprescindibles para mantener el equilibrio del ecosistema, y asegurar la supervivencia del resto de animales.
                                </p>
                                <p>
                                    Al Gobierno balear le reclaman que se declaren las islas como zona de "no consumo de tiburones". La carne de tintorera y marrajo se encuentra a menudo en la sección de congelados de grandes superficies. Aniol Esteban dice que "hay que concienciarse de que comer carne de tiburón es como comer carne de tigre o elefante".
                                </p>
                                <p>
                                    Proponen también que se repliquen iniciativas como las de destinos turísticos en Maldivas o el Caribe, donde la presencia de tiburones representa un atractivo importante para la economía local. En Canarias existe un turismo de buceo relacionado con estas especies que atrae, su acuerdo con Pew Research, logran recaudar un mínimo de tres millones de euros y donde genera casi un centenar puestos de trabajo.
                                </p>
