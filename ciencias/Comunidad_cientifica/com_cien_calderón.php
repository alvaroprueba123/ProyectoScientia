                                <h2>¿Podría la biodiversidad prevenir futuras pandemias?</h2>
                                                                
                                <br> Por Ana Camila Calderón Mayo
                                <img src="../imagenes/ciencias/comu_cientif/calderón/img3.jpg" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p><i>Fig. 1. REUTERS/Rupak De Chowdhuri “El coronavirus, no solo afecta a los humanos”</i></p>

                                <p>
                                En los últimos meses, el COVID-19 ha tomado cada vez más importancia debido a las consecuencias que ha generado en todo el mundo. De cierta forma todos nos hemos visto afectados, pero, ¿Qué hay sobre el ambiente, y la cantidad de especies que la habitan?
                                </p>
                                <p>
                                Un tema no muy mencionado pero muy relacionado con la pregunta, es la biodiversidad que, si bien engloba una amplia variedad de seres vivos en la tierra, incluyendo la flora y fauna, aún no se ha destacado el valioso rol que tiene en el control de las enfermedades infecciosas humanas.
                                </p>
                                <p>
                                Y aunque, con la aceleración de la pérdida de biodiversidad y el actual interés de la conciencia humana ante una enfermedad zoonótica que ha puesto en JAQUE MATE la salud pública mundial, surge una pregunta de postura ecóloga: ¿Sería capaz la biodiversidad de controlar enfermedades? Y una respuesta existente es llamado "efecto de dilución".
                                </p>

                                <p>
                                El efecto protector de la biodiversidad por “dilución”3, prácticamente genera un efecto de amortiguamiento ante la transmisión de enfermedades hacia los seres humanos. Y, ¿Cómo funciona? Pues, muchos estudios señalan que, si se pierde la biodiversidad aumentaría la abundancia de los huéspedes de enfermedades, (siendo un huésped el organismo que aloja a otro, o lo lleva consigo en su interior).
                                </p>
                                Por ejemplo, cuando se tiene un ecosistema que no ha sido afectado por la actividad humana, y se encuentra la biodiversidad intacta, es decir, coexistiendo de forma equilibrada, los patógenos se encuentran “diluidos”, y debido a las condiciones que posee, existirían pocas probabilidades de generar futuras enfermedades o pandemias. Pero, cuando un ecosistema ha sido intervenido o deforestado, existirían especies que empezarían a volverse más abundantes junto con alguna enfermedad, produciendo un riesgo para los demás miembros del ecosistema.. 
                                <p>
                                Asimismo, otros estudios, contradicen el efecto dilución pues apoyan la idea contraria de que aumentaría los agentes infecciosos, debido a que las enfermedades podrían disminuir a los huéspedes y no huéspedes, quienes actúan como barrera de protección y así aumentar posibles casos de zoonosis.  
                                </p>

                                <p>
                                Ante estos dos enfoques, dudamos sobre si la biodiversidad reduciría la amenaza ante los agentes patógenos. Pero, con el futuro, los resultados de enfermedades infecciosas no serán respuestas simples o consistentes. Y eso debido a que, ante los cambios antropogénicos durante muchos y largos años gran parte de los efectos negativos se lo ha llevado la naturaleza misma y con ella, la biodiversidad.
                                </p>
                                <img src="../imagenes/ciencias/comu_cientif/calderón/img4.jpg" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p><i>Fig.2 REUTERS/Yves Herman “El mundo depende de la naturaleza”</i></p>
                                <p>
                                Cada vez se adopta una perspectiva más general por parte de los ecologistas1. Quienes señalan que la disminución de la biodiversidad podría ser o no la causante de muchas nuevas enfermedades, pero la destrucción que provocamos ante ella no es la solución más amigable para mejorar la salud humana, porque, aunque nos cueste reconocer muchos de los ecosistemas si bien albergan diversidad de especies, al menos cumplen un papel importante debido a los servicios ambientales que ofrecen para el bienestar humano.
                                </p>
                                <p>
                                Un desafío como parte de nuestra generación a partir de ahora, será identificar oportunidades para el aprovechamiento y conservación de la biodiversidad, ya que a medida que el ser humano modifica el ecosistema como son las islas o puntos de biodiversidad, se pierde el hábitat y con eso los patógenos que pertenecen a la vida silvestre, empezarían a experimentar una rápida diversificación.
                                </p>
