                                <p>
                                ¿Qué es el infinito? Tratar de responder esta pregunta es un tanto tedioso, pues, de hecho, los mejores matemáticos de la actualidad continúan dándole vueltas al asunto. En simples palabras el concepto de infinito se resume a algo que no tiene fin ni límite, es una idea matemática. Exentamente a centrarnos en responder la pregunta como tal, lo mejor es primero desde un punto de vista histórico y su implicancia en nuestro día a día con el infinito (1).
                                </p>
                                <p>
                                Desde hace miles de años atrás, los seres humanos nos encontramos con la necesidad de hallar una forma de contabilizar los animales u objetos que teníamos a nuestro alrededor. Para realizar tal acción se hacía uso de otros objetos; sin embargo, se observó que era complicado enumerar cantidades demasiado grandes debido a la limitada concepción numérica que se comprendía en aquel entonces, por lo que si ocurría que la cantidad conocida era rebasada se denominaba como “mucho más” a la colectividad de la misma.
                                </p>
                                <p>
                                Es decir que, si una persona tenía muchas cosas en su haber y la cantidad superaba lo máximo que se podía contar, le decían que tenía “mucho más”, pero ¿qué pasaba si otra persona tenía más cosas que la anterior?, entonces a la cantidad anterior se decía que había un “mucho” mayor comparado con otro.
                                </p>

                                <p>
                                Si generalizamos esta idea y tomamos el infinito como algo muy grande ¿podrá existir algo más grande que un infinito? o en otras palabras, ¿existen infinitos más grandes que otros infinitos?
                                </p>
                                <p>
                                En nuestro día a día utilizamos los números, mejor dicho, las matemáticas, para cualquier cosa, desde ir al mercado a hacer compras hasta realizar una operación sobre cuál es la distancia entre dos o más planetas. Pese a todo ello, la idea de infinito no la reconocemos a simple vista, aunque esta se encuentre dentro de muchas cosas que nosotros vemos y decimos.
                                </p>
                                <p>
                                Pensemos en primera instancia en los conjuntos numéricos: en este caso tenemos a los naturales (N={1,2,3,…}), a los enteros (Z={…,-2,-1,0,1,2,,…}) y a los reales (R), los cuales vienen distribuidos en la inclusión de conjuntos de la siguiente forma: N⊂Z⊂R. Ahora definamos también lo que es la cardinalidad de un conjunto, que no es nada más y nada menos que la cantidad de elementos que tiene el mismo: A (card(A),#A), 
                                </p>
                                <p>
                                A simple vista podemos observar que la card(N) < card(Z)< card(R), ya que R tiene más elementos que Z y este último más elementos que N, pero no todo es como lo observamos. Se demuestra que la card(N)=∞=card(Z), ya que a pesar de que los N sean un subconjunto de los Z, tienen la misma cardinalidad. A este concepto de infinito, respecto a la cardinalidad de N y Z lo denominaremos Aleph sub 0 (ℵ_0), pero ¿qué sucede con R? Si bien card(R)=∞. Este infinito es un infinito más grande de lo que habíamos encontrado anteriormente con la cardinalidad de N y Z, por lo tanto, es la primera evidencia de que existen infinitos más grandes que otros. A este, respecto a la card(R), lo definiremos como Aleph sub 1 (ℵ_1). Entonces tendríamos que ℵ_0< ℵ_1, evidenciando nuevamente que aunque son dos infinitos, tienen diferencias respecto a su tamaño. En base a ello surge la duda, ¿existirán infinitos más grandes que ℵ_1?
                                </p>
                                <p>
                                En la actualidad, frases como: “Al infinito y más allá”, “Te amo infinitamente” o “Te amo hasta el infinito”, estas nos abren las puertas respecto a la idea de este término como algo habitual y fácil de reconocer. Inclusive pensar en un universo infinito, trae consigo la existencia de infinitos planetas como el nuestro, con infinitas realidades donde infinitas personas están leyendo esto e infinitas ideas vienen a la mente del sujeto. 
                                </p>
                                <p>
                                Otro completamente válido es el que se aplica en el curso de geometría cuando nos dicen que dos rectas paralelas nunca se cruzarán, sin embargo, esto depende del punto de vista ya que dos rectas paralelas sí se pueden cruzar, y el lugar en el que lo hacen es en el infinito y el ejemplo claro de ello se da en la geometría proyectiva. Un caso estrechamente relacionado a ello se da cuando vamos por la carretera y desde el punto donde nos encontramos vemos a lo lejos que la carretera se hace cada vez más pequeña, a pesar de que esta tenga como fronteras rectas paralelas en la lejanía se visualiza que llegan a intersecar. 
                                </p>
                                <p>
                                Hablar de infinito es adentrarse a un mundo lleno de curiosidades y extrañezas, donde tienes que abrir tu mente para poder captar la variedad de señales que tenemos en nuestro día a día. Así que la próxima vez que decidas decirle a tu pareja que la amas infinitamente, procura indicarle de qué infinito estás hablando. 
