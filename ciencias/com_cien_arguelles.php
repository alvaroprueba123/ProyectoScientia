                                <h2>EDICIÓN GENÉTICA</h2>
                                Rediseñando la vida con la Ingeniería Genética
                                <br>Por Alexis Argüelles Curaca
                                <img src="../imagenes/ciencias/comu_cientif/arguelles/img3.jpg" style="max-height :100%; max-width:100%; margin: auto; display:block; padding:20px 0px 20px 0px;">
                                <p><i>Modificación de genes que permite editar el genoma. O'neal Coto K. ¿Qué opina la ciudadanía y los científicos costarricenses sobre la edición genética? [Internet]. 2019 [citado 14 julio 2020]. Disponible en: https://url2.cl/TMlIv</i></p>

                                <p>
                                Desde siempre la diversidad de flora y fauna ha despertado la curiosidad en el ser humano; impulsándonos hacia la investigación y descubrimiento, utilizando instrumentos acordes a nuestro tiempo.
                                </p>
                                <p>
                                Desde entonces la investigación no se ha detenido, dando resultado al avance de la ciencia, perfeccionando y rediseñando las tecnologías que han contribuido al conocimiento humano. 
                                </p>
                                <p>
                                El siglo XXI trajo consigo la secuenciación del genoma humano que representa nuestra ‘‘receta molecular’’, a la cual llamaremos ADN (ácido desoxirribonucleico) (1); a la par de este descubrimiento se elaboraron tecnologías que la permitieron manipular el ADN. 
                                </p>

                                <p>
                                La ingeniería genética surgió para describir investigaciones que incluían la tecnología del ADN recombinante cuyos orígenes datan de los años 70, Genómica y genética moderna del siglo XXI (2) (3). Mediante la manipulación de tecnologías de ingeniería genética se logró descubrir un mecanismo de defensa en bacterias, el cual modifica su genoma para  reconocer ataques virales, este mecanismo fue posteriormente incluido en el desarrollo de una herramienta de edición genética conocida como CRISPR-Cas (Repeticiones Palindrómicas Cortas Agrupadas y Regularmente Interespaciadas) (4)
                                </p>
                                <p>
                                La primera edición con CRISPR-Cas9 se dio en 2012 por un grupo de la Universidad de Berkeley liderado por Jennifer Doudna y Emmanuelle Charpentier, posteriormente un equipo perteneciente a la Universidad de Harvard y al MIT (Instituto Tecnológico de Massachusetts) encabezado por Freg Zhang demostró la utilidad de esta herramienta para editar el genoma de especies vivas, resaltando la especificidad con la que se realiza esta edición, llegando a modificar pequeños ingredientes de la ya mencionada receta molecular de un organismo, usando enzimas que guiaran a un sitio de corte especifico (1). Esto tuvo un gran impacto en la comunidad científica y en la ingeniería genética ya que acogieron esta herramienta por su versatilidad al ser aplicable en diferentes organismos permitiendo reescribir el ADN. (5)
                                </p>
                                <p>
                                Sus aplicaciones son diversas: Pudiendo reparar genes defectuosos que causan enfermedades hereditarias, generar una mayor respuesta inmune a enfermedades oncológicas y modificando bacterias propias de nuestro organismo para mejorar su rol simbiótico; pero desde el punto de vista ético la modificación genética en humanos es muy cuestionada (6). Diversas Instituciones se han pronunciado en contra de la manipulación de ADN en células germinales humanas como el Comité Internacional de Bioética(CIB) en abril del 2003 y julio del 2017, actualizando en este año las reflexiones del CIB sobre el genoma humano y los derechos humanos, en la cual se expresa que los avances científicos deben realizarse bajo normas éticas y considerando los derechos humanos (7). Las aplicaciones de la edición genética con CRISPR-Cas en organismos vegetales es más aceptada por la comunidad científica habiendo múltiples investigaciones al respecto para mejorar la producción y sequías de cultivo con importancia agronómica industrial (8).
                                </p>
                                <p>
                                En el Perú también se ha optado por esta tecnología, un grupo de la Universidad San Martin de Porres se encuentra investigando la causa genética de una enfermedad que afecta a células de la retina ocasionando ceguera, y pretenden corregir la mutación causal de la ceguera usando CRISPR-Cas9 (9).
                                </p>
                                <p>
                                Esta herramienta biotecnológica de CRISPR-Cas, desde el descubrimiento de su potencial no ha dejado de superar los limites de lo que creíamos posible, pero por cada promesa de erradicar enfermedades, maximizar la cosecha en la agricultura y producir drogas milagrosas hay un número igual de historias de distopia sobre la soberbia humana llevándolas demasiado lejos; la comunidad científica debe abordar esta herramienta muy cuidadosamente, para evitar que el potencial benéfico de esta herramienta se vea afectado por problemas éticos. 
                                </p>
