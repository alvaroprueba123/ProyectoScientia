                                <p style="text-align: center;">
                                Las Becas Erasmus Mundus ofrecen una oportunidad única a destacados estudiantes en Latinoamérica. Fuente: Becas Erasmus Mundus
                                </p>
                                <p>
                                Desde su creación en 1987 el programa Erasmus Mundus (European Region Action Scheme for the Mobility of University Students) financiada por la Unión Europea; cuenta con un plan de gestión donde los estudiantes acreedores podrán adquirir las habilidades necesarias para contribuir a su desarrollo profesional y para profundizar en su comprensión de otras culturas. Aspiran igualmente a potenciar las capacidades, atractivo y dimensión internacional de las organizaciones que participan1. Aunque al inicio estas becas fueron ofrecidas exclusivamente para estudiantes europeos, con el paso de los años diferentes países y gobiernos alrededor del mundo fueron ofreciendo su respaldo. Es a partir de la cooperación y del éxito que tuvo el programa, que en la actualidad se conocen las Becas Erasmus Mundus, van dirigidas a estudiantes de todo el mundo, incluidos países Latinoamericanos como Argentina, Bolivia, Brasil, Colombia, Costa Rica, Cuba, Ecuador, El Salvador, Guatemala, Honduras, México, Nicaragua, Panamá, Paraguay, Venezuela y Perú2.
                                <br>
                                Los beneficios más resaltantes que ofrece esta beca, en primera instancia es integral, es decir cubre los gastos relacionados con el viaje y visado, el pago de la matrícula y programa de formación en la universidad a la que se postule y servicios de seguros médicos. Así mismo, brinda un monto mensual al estudiante durante el tiempo de duración del programa a donde postule, puede variar entre 3 y 12 meses. Al finalizar el programa el estudiante obtendrá créditos que luego se reconocen en la institución de origen como parte de su grado. Cabe señalar, otra gran ventaja es que la beca no es de retorno, lo que significa que después de acabar los estudios no es obligatorio regresar al país de origen por uno o dos años; sin duda una gran ventaja si se desea continuar estudiando en el país, o en la Unión Europea3.
                                </p>
