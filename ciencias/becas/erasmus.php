                                <h5>REQUISITOS PARA APLICAR A LA BECA.</h5>
                                <br>
                                En contraste con otros programas de becas, los requisitos para obtener una beca Erasmus son más accesibles por la mayoría de jóvenes. A continuación, se listará los principales requisitos4:
                                <br>
                                -	El primer requisito es que la universidad de procedencia tenga convenio o acuerdos de intercambio estudiantil con universidades europeas. 
                                -	El compromiso del estudiante con el programa es muy importante, puesto que en caso de invertir de manera inadecuada el subsidio de manutención estará obligado a devolver la totalidad del mismo.
                                <br>
                                -	Es muy importante NO haber vivido por ningún motivo en uno de los países europeos donde desean estudiar por más de 12 meses durante los últimos 5 años. Ni tener nacionalidad de un país de la Unión Europea.
                                <br>
                                -	Tener suficiencia en el idioma del país oferente de la beca. Por lo general para la mayoría de programas el idioma es el inglés de manera que se deberá presentar el TOEFL o el IELTS; sin embargo, hay varios programas sobre todo en el área de derecho o de humanidades, donde puede ser necesario el conocimiento de otros idiomas, como el francés, alemán u italiano. Los criterios exactos los dan en las convocatorias.
                                <br>
                                -	Ser aceptado en un programa académico de las universidades participantes. Para saber con exactitud las fechas de las convocatorias es importante estar pendiente del catálogo (link) de la plataforma, puesto que las becas son gestionadas directamente desde la plataforma de la comisión europea, o bien, es liderada a través de las mismas instituciones. 
                                <br>
                                -	Por último, para ser aceptado por la universidad de interés, cada programa pedirá completar una solicitud online para aplicar al programa y adjuntar los documentos requeridos que son el CV, carta de motivación, certificados de notas del grado universitario obtenido hasta entonces, cartas de recomendación
                                <br>
                                Como mencionó el representante de la Asociación Erasmus Mundus de la Unión Europea-Delegación Peruana, Nicola Tarque5 “la ventaja del becario frente a sus compañeros es que retornará al país de procedencia con un nuevo aprendizaje académico y cultural, así como posibles contactos que haya podido hacer. Se le abre un mundo de posibilidades”
                                </p>
                                
