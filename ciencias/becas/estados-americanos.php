                                <p>
                                La Organización de los Estados Americanos (OEA) fue creada en 1948 y reúne a los 35 Estados independientes de las Américas. Esta organización cuenta con un programa de becas académicas que fue establecido en 1958 y cada año otorga becas para maestrías, doctorados o investigación de postgrado orientados a la obtención de un título universitario de alguna universidad o institución de educación superior acreditados en alguno de los Estados Miembros de la OEA, con excepción de su país patrocinador, en este caso, Perú [1].
                                </p>
                                <h5>BENEFICIOS DE LA BECA</h5>
                                <p>
                                Las becas tienen un tope monetario de US$10,000 anuales por estudiante para cubrir estudios de posgrado o investigación de postgrado por un mínimo de un año y un máximo de dos. Dichos fondos cubren la matrícula y tasas administrativas obligatorias, seguro de salud, asignación anual de materiales de estudio, aporte complementario mensual de subsistencia y boleto aéreo de ida y vuelta [2].
                                </p>
                                <h5>REQUISITOS DE LA BECA Y FORMA DE POSTULACIÓN</h5>
                                <p>
                                Entre los requisitos para postular a esta beca se encuentran [2]:
                                -	Ser peruano o residente permanente legal.
                                <br>
                                -	Estar en buen estado de salud físico y mental.
                                <br>
                                -	Dominar el idioma del país o del programa de estudio.
                                <br>
                                -	Comprometerse a regresar al Perú y residir por un período no menor a 24 meses tras culminar sus estudios. 
                                <br>
                                -	Contar con un título universitario al momento de postular a la beca.
                                <br>
                                -	Estar matriculado en un programa de estudios o tener una carta de admisión a la universidad donde se planea estudiar (que debe ser diferente al país patrocinador del postulante).
                                </p>
                                <p>
                                Para postular, se debe completar el formulario de solicitud en línea (https://www.oas.org/fms/Announcement.aspx?id=1093&Type=1&Lang=Spa) y enviar los documentos requeridos al correo electrónico gestion.becas@pronabec.gob.pe con el asunto Programa de Becas Académicas de la OEA 2020. Los documentos requeridos son [2,3]: 
                                <br>
                                -	Copia del diploma de estudios universitarios del título más alto obtenido.
                                <br>
                                -	Copia del certificado de calificaciones recibidas por el título más alto obtenido.
                                <br>
                                -	Prueba de matrícula en el programa de estudios de posgrado o carta de admisión.
                                <br>
                                -	Dos cartas de recomendación.
                                <br>
                                -	Currículum Vitae no documentado.
                                <br>
                                -	Copia del documento oficial de identidad. 
